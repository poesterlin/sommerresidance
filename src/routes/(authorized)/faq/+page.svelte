		Wir hoffen, dass es nicht benötigt wird, aber sollte es einen Notfall geben, dann wendet euch bitte an eines der
		Geburtstagskinder (Theresa, Baschdi oder Sarina) oder an
	</span>
	<span>Hedwig (Theresas Mama) oder Erich (Sarinas Papa)</span>
	<span>.</span>
</p>
