<h1><span>FAQs</span></h1>
<ol start="1">
	<li>
		<span>Datum, Zeit und Ort der SOMMERRESI</span>
		<span>dance</span>
	</li>
</ol>
<p>
	<span>Wir freuen uns, euch alle zu unserem einzigartigen Festival zu begrüßen, das am Wochenende vom</span>
	<span class="b">26. bis 28.07.2024</span>
	<span>&nbsp;stattfindet.</span>
</p>
<p>
	<span class="b">Anreise:</span>
	<span>
		Freitag 12:00 - 17:00 Uhr*
		<br />
	</span>
	<span class="b">Official Start:</span>
	<span>Freitag</span>
	<span>
		18:00 Uhr
		<br />
	</span>
	<span class="b">Abreise:</span>
	<span>Sonntag bis 16:00 Uhr</span>
</p>
<p>
	<span class="b">Ort:</span>
	<span>Hauptstraße 45, 87787 Wolfertschwenden (weitere Details findet ihr unter</span>
	<a>Anfahrt LINK</a>
	<span>)</span>
</p>
<p>
	<span>
		*Wir sind am Freitag den ganzen Tag vor Ort, das heißt, ihr könnt auch schon vor 12:00 Uhr anreisen. Gebt uns dann
		einfach kurz Bescheid. Natürlich ist auch eine Anreise nach 17:00 Uhr möglich (und am Samstag). Wir würden am
		Freitag aber gerne um 18:00 Uhr “offiziell” starten, denn danach gibt es schon die ersten Acts. Daher freuen wir
		uns, wenn ihr früh anreist und die Acts supported :)
	</span>
</p>
<ol start="2">
	<li>
		<span>Anreise und Parken</span>
	</li>
</ol>
<p>
	<span class="b">Mit dem Auto:</span>
	<span>
		Vor Ort haben wir eine große Parkfläche, direkt gegenüber der Hauptstraße 45 (siehe Lageplan). Diese wird gut
		ausgeschildert sein. Bitte parkt dort platzsparend, damit möglichst viele Autos hinpassen. Sollte der Parkplatz voll
		sein, ladet vor Ort euer Zeug aus und parkt dann bitte bei der Festhalle Wolfertschwenden (Am Sportplatz 9, 87787
		Wolfertschwenden). Von dort sind es 3 Minuten Fußweg bis zum Festival.
	</span>
</p>
<p>
	<span class="b">Mit dem Zug:</span>
	<span>
		&nbsp;Fahrt zur Station Bahnhof Bad Grönenbach (ca. 2km vom Festival entfernt). Wer möchte, kann von dort also zum
		Festival laufen ;) Da ihr aber vermutlich viel Gepäck dabei habt, werden wir für euch ein Shuttle organisieren.
		Bitte meldet euch vorab bei uns, wenn ihr mit dem Zug anreist, damit wir das planen können.
	</span>
</p>
<p>
	<span class="b">Zu Fuß oder mit dem Fahrrad:</span>
	<span>Eine Anreise zu Fuß oder mit dem Fahrrad ist auch möglich. Beachtet hierbei bitte eure Reisezeit.</span>
</p>
<p>
	<span class="b">Fahrgemeinschaften:</span>
	<span>Falls ihr von weiter weg anreist, tretet doch dieser Whats-App Gruppe bei:</span>
	<a>LINK</a>
	<span>
		&nbsp;Dann könnt ihr Fahrgemeinschaften bilden. Es kommen bspw. einige Leute aus Hamburg, Stuttgart, Konstanz,
		Augsburg, München.
	</span>
</p>
<ol start="3">
	<li><span>Verpflegung und Getränke</span></li>
</ol>
<p>
	<span>
		Wie auf jedem guten Festival seid ihr auf euch selbst gestellt, was eure Verpflegung angeht. Eine gewisse Grundlage
		an Getränke- und Essensvorrat werden wir aber natürlich bereitstellen. Für die weitere Verpflegung vor Ort befindet
		sich gleich neben dem Gelände ein Bäcker und ein Metzger. In 5 Minuten ist der Dorfladen Wolfertschwenden zu Fuß
		erreichbar. Mit dem Auto ist man in 5 Minuten in Bad Grönenbach, wo es Netto und Edeka gibt. Wir werden einen Grill
		bereitstellen, den ihr benutzen dürft. Es werden euch natürlich auch Kühlmöglichkeiten und Trinkwasser zur Verfügung
		gestellt. Gaskocher und weiteres Camping-Equipment dürft ihr gerne mitbringen, sie sind auf dem Festival- und
		Campinggelände erlaubt. Für weitere Infos, was ihr (nicht) mitbringen müsst, schaut bitte unbedingt in die Packliste
		auf der Seite
	</span>
	<span>Camping LINK</span>
	<span>.</span>
</p>
<ol start="4">
	<li><span>Sanitärbereich</span></li>
</ol>
<p>
	<span>
		Wir werden einen Klowagen organisieren, den ihr jederzeit benutzen dürft und müsst. Zuwiderhandlungen haben
		schwerwiegende Folgen - mehr dazu in unseren
	</span>
	<span>AGBs</span>
	<span>
		. &nbsp; Eine Außendusche mit Sichtschutz wird ebenfalls hergerichtet. Bitte bringt hierfür biologisches
		Duschgel/Shampoo mit, wenn ihr es vorhanden habt. Wir werden für die restlichen Personen einen Vorrat an
		biologischem Duschgel/Shampoo bereitstellen. Herkömmliches Duschgel/Shampoo darf in der Außendusche nicht verwendet
		werden.
	</span>
</p>
<ol start="5">
	<li>
		<span>Unterkünfte und Campingmöglichkeiten</span>
	</li>
</ol>
<p>
	<span>
		Für ein perfektes Festivalfeeling, darf das Camping natürlich nicht fehlen. Hierfür stellen wir einen extra
		Zeltplatz neben dem Festivalgelände zur Verfügung. Bitte organisiert euch eure Zelte und Schlafutensilien selbst.
		Wer in seinem Auto übernachten möchte, kann dies bei schönem Wetter auf dem Zeltplatz tun. Bei schlechtem Wetter
		oder wenn ihr beispielsweise Kinder dabei habt, könnt ihr unseren Parkplatz gegenüber nutzen. Hier ist nachts
		besonders auf die Lautstärke zu achten, da direkt nebenan Nachbarn sind. Falls Camping für euch keine Option ist,
		könnt ihr bei Harry’s Gästehaus in Wolfertschwenden schauen, ob noch ein Platz frei ist. Weitere Unterkünfte gibt es
		in Bad Grönenbach und Ottobeuren (beides ist nur mit dem Auto zu erreichen).
	</span>
</p>
<ol start="6">
	<li><span>Programm und Ablauf</span></li>
</ol>
<p>
	<span>Um auf dem aktuellen Stand zu sein, haltet euch über die Seite</span>
	<span>&nbsp;</span>
	<span>Lineup</span>
	<span>
		auf dem Laufenden. Generell werden wir euch tagsüber mit Workshops und Spielen bei Laune halten. Für jede*n wird
		hier was dabei sein! Bis ca. 22:00 Uhr, werden euch unsere DJ’s und Bands mit Musik verwöhnen. Um den Nachbarn nicht
		sämtliche Nerven zu rauben, könnt ihr euch danach in unserem Partyzelt die Seele aus dem Leib tanzen oder am
		Lagerfeuer chillen.
	</span>
</p>
<ol start="7">
	<li><span>Wetterbedingungen</span></li>
</ol>
<p>
	<span>
		Die Wettervorhersage, sieht jetzt schon perfekt aus! Daher vergesst nicht, euch auf die Sonne vorzubereiten! Bringt
		genügend Sonnenschutz und Sonnencreme mit. Bitte denkt daran, dass auch eine ausreichende Hydrierung wichtig ist! An
		der Wasserstelle könnt ihr euch mit frischem Leitungswasser versorgen.
	</span>
</p>
<p>
	<span>
		Falls das Wetter doch unerwartet schlecht werden sollte, geben wir unser Bestes, um euch trocken durch das
		Wochenende zu bringen. Aber ohne euch sind wir hier leider aufgeschmissen. Wenn ihr schon im Vorraus seht, dass
		schlechtes Wetter auf uns zukommen wird, deckt euch mit ausreichend trockenen Klamotten ein und denkt in jedem Fall
		daran, einen Regenschutz mitzubringen.
	</span>
</p>
<ol start="8">
	<li>
		<span>Sicherheit und Notfallkontakte</span>
	</li>
</ol>
<p>
	<span>
